     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $producto=new Producto();
        $producto->nombre_producto=$request->nombre_producto;
        $producto->descripcion_producto=$request->descripcion_producto;
        $producto->historia_producto=$request->historia_producto;
        $producto->imagen_producto=$request->imagen_producto;
        $producto->fecha_produccion-$request->fecha_produccion;
        $producto->fecha_vencimiento=$request->fecha_vencimiento;
        $producto->estado_producto=$request->estado_producto;
        $producto->presentacion_producto=$request->presentacion_producto;
        $producto->categoria=$request->categoria;

        $producto->save();
        return redirect()->route('Producto.create');
    }

